#!/usr/bin/env python3
"""
export_pca_dataset_from_2d.py

Builds a PCA-ready dataset (X.npy + metadata.csv) from Option-1 2D exports
generated by synthetic_trabecular_v2_voting3d.py.

Typical inputs:
  data/v2_voting3d_option1/volumes.csv
  data/v2_voting3d_option1/vol_XXXXX_mip.png (or gray_mip)

Outputs:
  <outdir>/X.npy
  <outdir>/metadata.csv
"""

from pathlib import Path
import argparse
import csv

import numpy as np
from PIL import Image


def read_csv_rows(csv_path: Path):
    with open(csv_path, "r", newline="") as f:
        return list(csv.DictReader(f))


def load_png_as_flat(path: Path) -> np.ndarray:
    img = Image.open(path).convert("L")
    arr = np.array(img, dtype=np.uint8)
    return arr.reshape(-1).astype(np.float32)


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--gen-dir", type=str, default="data/v2_voting3d_option1",
                    help="Directory containing volumes.csv and the exported PNGs.")
    ap.add_argument("--outdir", type=str, default="data/pca",
                    help="Output directory for X.npy + metadata.csv.")
    ap.add_argument("--use", type=str, default="mip",
                    choices=["mid", "mip", "mean", "gray_mid", "gray_mip"],
                    help="Which 2D representation to use as PCA input.")
    ap.add_argument("--limit", type=int, default=0,
                    help="If >0, limit number of samples.")
    args = ap.parse_args()

    gen_dir = Path(args.gen_dir)
    outdir = Path(args.outdir)
    outdir.mkdir(parents=True, exist_ok=True)

    csv_path = gen_dir / "volumes.csv"
    if not csv_path.exists():
        raise FileNotFoundError(f"Missing {csv_path}")

    rows = read_csv_rows(csv_path)
    if args.limit and args.limit > 0:
        rows = rows[:args.limit]

    key_map = {
        "mid": "mid_png",
        "mip": "mip_png",
        "mean": "mean_png",
        "gray_mid": "gray_mid_png",
        "gray_mip": "gray_mip_png",
    }
    col = key_map[args.use]

    X_list = []
    meta_out = []

    for r in rows:
        rel = (r.get(col) or "").strip()
        if not rel:
            # skip rows where export not present
            continue
        img_path = gen_dir / rel
        if not img_path.exists():
            # skip missing file
            continue

        x = load_png_as_flat(img_path)
        X_list.append(x)

        # store metadata (keep all generator CSV fields + add input file)
        r2 = dict(r)
        r2["pca_input_mode"] = args.use
        r2["pca_input_file"] = rel
        meta_out.append(r2)

    if not X_list:
        raise RuntimeError(
            f"No samples collected. Check that you ran generator with --export-2d "
            f"and that column {col} exists in volumes.csv."
        )

    X = np.stack(X_list, axis=0)
    np.save(outdir / "X.npy", X)

    # write metadata.csv
    meta_path = outdir / "metadata.csv"
    fieldnames = list(meta_out[0].keys())
    with open(meta_path, "w", newline="") as f:
        w = csv.DictWriter(f, fieldnames=fieldnames)
        w.writeheader()
        for r in meta_out:
            w.writerow(r)

    print(f"Saved PCA dataset:")
    print(f"  X: {outdir / 'X.npy'}  shape={X.shape}")
    print(f"  metadata: {meta_path}")


if __name__ == "__main__":
    main()
